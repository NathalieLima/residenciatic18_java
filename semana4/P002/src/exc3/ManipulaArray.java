package exc3;

import java.util.ArrayList;

public class ManipulaArray {
	private ArrayList<int> numeros;
	
	public static int soma() {
		int soma;
		
		for (int i = 0; i < numeros.size(); i++) {
			soma += numeros[i];
		}
		
		return soma;
	}
	
	public static int soma() {
		int soma;
		
		for (int i = 0; i < numeros.size(); i++) {
			soma += numeros[i];
		}
		
		return soma;
	}
}
